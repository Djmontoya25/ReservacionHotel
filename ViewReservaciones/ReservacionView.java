    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
            String sql = "SELECT r.id, h.nombre, h.apellido, hab.tipo, r.fecha_entrada, r.fecha_salida, r.estado " +
                    "FROM reservas r " +
                    "INNER JOIN huespedes h ON r.id_huesped = h.id " +
                    "INNER JOIN habitaciones hab ON r.id_habitacion = hab.id " +
                    "WHERE r.id = ?";

       try (Connection con = ConexionDB.getConnection();
            PreparedStatement pst = con.prepareStatement(sql)) {

           reservacionId = Integer.parseInt(txtReservacion.getText().trim());
           pst.setInt(1, reservacionId);

           ResultSet rs = pst.executeQuery();

           if (rs.next()) {
               txtNombre.setText(rs.getString("nombre"));
               txtHabitacion.setText(rs.getString("tipo"));
               txtCheckIn.setText(rs.getString("fecha_entrada"));
               txtCheckOut.setText(rs.getString("fecha_salida"));
               txtEstado.setText(rs.getString("estado"));
           } else {
               JOptionPane.showMessageDialog(this, "No se encontró una reserva con el ID: " + reservacionId);
           }
       } catch (SQLException e) {
           e.printStackTrace();
           JOptionPane.showMessageDialog(this, "Error al buscar la reserva: " + e.getMessage());
       } catch (NumberFormatException e) {
           JOptionPane.showMessageDialog(this, "El ID debe ser un número válido.");
       }  
    }//GEN-LAST:event_btnBuscarActionPerformed

