    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sql = "INSERT INTO reservas (id_huesped, id_habitacion, fecha_entrada, fecha_salida, estado) " +
                 "VALUES (?, ?, ?, ?, 'pendiente')";

        try (Connection con = ConexionDB.getConnection();
             PreparedStatement pst = con.prepareStatement(sql)) {

            idHuesped = Integer.parseInt(txtIdHuesped.getText().trim());
            HabitacionId = Integer.parseInt(txtHabitacion.getText().trim());
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date checkInUtil = sdf.parse(txtCheckIn.getText().trim());
            java.util.Date checkOutUtil = sdf.parse(txtCheckOut.getText().trim());

            java.sql.Date checkInSQL = new java.sql.Date(checkInUtil.getTime());
            java.sql.Date checkOutSQL = new java.sql.Date(checkOutUtil.getTime());

            pst.setInt(1, idHuesped);
            pst.setInt(2, HabitacionId);
            pst.setDate(3, checkInSQL );
            pst.setDate(4, checkOutSQL);

            int filasAfectadas = pst.executeUpdate();

            if (filasAfectadas > 0) {
                JOptionPane.showMessageDialog(this, "Reservacion registrada correctamente.");
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo registrar la reserva.");
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al registrar la reserva: " + e.getMessage());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Los IDs deben ser números válidos.");
        } catch (ParseException ex) {
            Logger.getLogger(ReservacionView.class.getName()).log(Level.SEVERE, null, ex);
        }  
    }//GEN-LAST:event_jButton1ActionPerformed

