    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
         String sql = "SELECT * FROM habitaciones WHERE id = ?";
    
        try (Connection con = ConexionDB.getConnection();
             PreparedStatement pst = con.prepareStatement(sql)) {

            id = Integer.parseInt(txtNumHabitacion.getText().trim()); // Obtener ID
            pst.setInt(1, id);

            ResultSet rs = pst.executeQuery();

            if (!rs.isBeforeFirst()) { // Verifica si hay datos
                JOptionPane.showMessageDialog(this, "No se encontraron registros con el ID: " + id);
                return;
            }

            while (rs.next()) {
                txtPrecio.setText(rs.getString("precio"));
                txtCapacidad.setText(rs.getString("capacidad"));
                txtEstado.setText(rs.getString("estado"));

            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al buscar el huÃ©sped: " + e.getMessage());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID debe ser un nÃºmero vÃ¡lido.");
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

