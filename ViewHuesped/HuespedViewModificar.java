    private void idTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idTextActionPerformed
        String sql = "SELECT * FROM huesped WHERE id = ?";
        try (Connection con = ConexionDB.getConnection();
             PreparedStatement pst = con.prepareStatement(sql)) {

            pst.setInt(1, id);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                nombreText.setText(rs.getString("nombre"));
                apellidoText.setText(rs.getString("apellido"));
                emailText.setText(rs.getString("email"));
                telefonoText.setText(rs.getString("telefono"));
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró un huésped con el ID proporcionado.");
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al buscar el huésped: " + e.getMessage());
        }                // TODO add your handling code here:
    }//GEN-LAST:event_idTextActionPerformed

