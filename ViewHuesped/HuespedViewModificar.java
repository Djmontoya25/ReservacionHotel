    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String sql = "SELECT * FROM huespedes WHERE id = ?";
    
        try (Connection con = ConexionDB.getConnection();
             PreparedStatement pst = con.prepareStatement(sql)) {

            int id = Integer.parseInt(idText.getText().trim()); // Obtener ID
            pst.setInt(1, id);

            ResultSet rs = pst.executeQuery();

            if (!rs.isBeforeFirst()) { // Verifica si hay datos
                JOptionPane.showMessageDialog(this, "No se encontraron registros con el ID: " + id);
                return;
            }

            while (rs.next()) {
                nombreText.setText(rs.getString("nombre"));
                apellidoText.setText(rs.getString("apellido"));
                emailText.setText(rs.getString("email"));
                telefonoText.setText(rs.getString("telefono"));
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al buscar el huésped: " + e.getMessage());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID debe ser un número válido.");
        }

    
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String sql = "UPDATE huespedes SET nombre = ?, apellido = ?, email = ?, telefono = ? WHERE id = ?";

        try (Connection con = ConexionDB.getConnection();
             PreparedStatement pst = con.prepareStatement(sql)) {

            id = Integer.parseInt(idText.getText().trim()); // Obtener ID
            pst.setString(1, nombreText.getText().trim());
            pst.setString(2, apellidoText.getText().trim());
            pst.setString(3, emailText.getText().trim());
            pst.setString(4, telefonoText.getText().trim());
            pst.setInt(5, id);

            int filasAfectadas = pst.executeUpdate();

            if (filasAfectadas > 0) {
                JOptionPane.showMessageDialog(this, "Huésped modificado correctamente.");
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró un huésped con el ID: " + id);
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al modificar los datos: " + e.getMessage());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID debe ser un número válido.");
            setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

